
public class Coin {
	private int Position ;
	private int Weight;
	public Coin(){}
	public Coin(int x,int y){
		this.Position = x;
		this.Weight = y;
	}
	public int getPosition() {
		return Position;
	}
	public void setPosition(int position) {
		this.Position = position;
	}
	public int getWeight() {
		return Weight;
	}
	public void setWeight(int weight) {
		this.Weight = weight;
	}
	
	

}
